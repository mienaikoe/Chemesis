	public static List<Chemical> find(String query){
		query = query.toUpperCase();
		List<Chemical> results = new ArrayList<Chemical>();
		for( Chemical chemical : Chemical.values() ){
			if( chemical.name.toUpperCase().equals(query) ){
				results.add(0, chemical);
			} else if( chemical.formulaQuery.matches(query) ) {
				results.add(0, chemical);
			} else if( chemical.queryMatch.contains(query) ){
				results.add(chemical);
			}
		}
		return results;
	}




	private static HashMap<Element, Integer> parseComposition(String formula){
		Pattern FORMULA_SPLIT_REGEX = Pattern.compile("([A-Z][a-z]*)(\\d*)");
		HashMap<Element, Integer> composition = new HashMap<Element, Integer>();
		Matcher m = FORMULA_SPLIT_REGEX.matcher(formula);
		while(m.find()){
			Element element = Element.valueOf(m.group(1));
			if( element != null ){
				String countStr = m.group(2);
				Integer count = 1;
				if( countStr.length() > 0 ) {
					count = Integer.valueOf(countStr);
				}
				composition.put(element, count);
			}
		}
		return composition;
	}

	private static String formatFormula(String formula){
		StringBuilder newFormula = new StringBuilder();
		for( char ch : formula.toCharArray() ){
			if( ch >= '0' && ch <= '9' ){
				newFormula.append((char) (ch + 8272));
			} else {
				newFormula.append(ch);
			}
		}
		return newFormula.toString();
	}
	
	private static String parseState(String state){
		String[] stateParts = state.split("_");
		StringBuilder ret = new StringBuilder();
		boolean first = true;
		for( String part : stateParts ){
			if( !first ){
				ret.append(", ");
			}
			first = false;
			switch(part){
				case "vit":
					ret.append("vitreous"); break;
				case "cd":
					ret.append("condensed"); break;
				case "fl":
					ret.append("fluid"); break;
				case "g":
					ret.append("gas"); break;
				case "sat":
					ret.append("saturated"); break;
				case "l":
					ret.append("liquid"); break;
				case "am":
					ret.append("amorophous"); break;
				case "pol":
					ret.append("polymeric"); break;
				case "mon":
					ret.append("monomeric"); break;
				case "cr":
					ret.append("crystalline"); break;
				case "aq":
					ret.append("aqueous solution"); break;
				case "ref":
					ret.append("reference state"); break;
				case "s":
					ret.append("solid"); break;
				case "sln":
					ret.append("solution"); break;
			}
		}
		return ret.toString();
	}
